/**
 * Get icon configuration based on section name
 * Matches the React Native implementation for consistency
 */
export function getSectionIcon(sectionName: string): SectionIconConfig {
  const nameLower = sectionName.toLowerCase();

  if (nameLower.includes('age')) {
    return { icon: <AgeIcon />, color: '#8B5CF6', bgColor: 'bg-purple-50' };
  }
  if (nameLower.includes('qualif')) {
    return { icon: <QualificationIcon />, color: '#10B981', bgColor: 'bg-emerald-50' };
  }
  if (nameLower.includes('fee') || nameLower.includes('payment')) {
    return { icon: <FeeIcon />, color: '#F59E0B', bgColor: 'bg-amber-50' };
  }
  if (nameLower.includes('date') || nameLower.includes('deadline')) {
    return { icon: <DateIcon />, color: '#3B82F6', bgColor: 'bg-blue-50' };
  }
  if (nameLower.includes('vacanc') || nameLower.includes('post')) {
    return { icon: <VacancyIcon />, color: '#6366F1', bgColor: 'bg-indigo-50' };
  }
  if (nameLower.includes('salary') || nameLower.includes('pay')) {
    return { icon: <SalaryIcon />, color: '#059669', bgColor: 'bg-green-50' };
  }
  if (nameLower.includes('link')) {
    return { icon: <LinkIcon />, color: '#2563EB', bgColor: 'bg-blue-50' };
  }
  if (nameLower.includes('how to apply') || nameLower.includes('application')) {
    return { icon: <HowToApplyIcon />, color: '#EC4899', bgColor: 'bg-pink-50' };
  }
  if (nameLower.includes('selection')) {
    return { icon: <SelectionIcon />, color: '#22C55E', bgColor: 'bg-green-50' };
  }
  
  // Default
  return { icon: <InfoIcon />, color: '#2563EB', bgColor: 'bg-blue-50' };
}
